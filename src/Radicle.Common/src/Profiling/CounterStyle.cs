namespace Radicle.Common.Profiling;

/// <summary>
/// Enumeration of styles for counters.
/// </summary>
public enum CounterStyle
{
    /// <summary>
    /// Counters are presented by their numeric value.
    /// </summary>
    Numeric = 0,

    /// <summary>
    /// Counters are presented as time spans.
    /// </summary>
    TimeSpan = 1,
}
